# Function which return the result
def func(a, b):
    # According to the observations
    # return the multiplication of both the numbers
    return a*b


# Given examples
assert (func(0, 20) == 0)
assert (func(1, 10) == 10)
assert (func(2, 5) == 10)
assert (func(3, 2) == 6)
assert (func(4, 1) == 4)
